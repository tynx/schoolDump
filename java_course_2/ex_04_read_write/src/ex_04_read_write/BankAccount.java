package ex_04_read_write;

/**
 * This class is very simple with 3 public attributes and represents
 * a bank account with an id, a balance and the date of creatoin
 * 
 * @author tim luginbuehl (lugtim@gmail.com // tynx.ch)
 * @author jerome jamin (jerome.jamin@gmail.com)
 */
public class BankAccount {

	/**
	 * The number/id of the account
	 */
	public int accountNumber = 0;

	/**
	 * The balance currently on the account
	 */
	public double currentBalance = 0.0d;

	/**
	 * The date of creation in the form of a string
	 */
	public String creationDate = null;
}
